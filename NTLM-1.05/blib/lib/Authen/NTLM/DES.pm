sub E_P16
{
  my ($p14) = @_;
  my $sp8 = [0x4b, 0x47, 0x53, 0x21, 0x40, 0x23, 0x24, 0x25];

  my $p7 = substr($p14, 0, 7);
  my $p16 = smbhash($sp8, $p7);
  $p7 = substr($p14, 7, 7);
  $p16 .= smbhash($sp8, $p7);
  return $p16;
}

sub E_P24
{
  my ($p21, $c8_str) = @_;
  my @c8 = map {ord($_)} split(//, $c8_str);
  my $p24 = smbhash(\@c8, substr($p21, 0, 7));
  $p24 .= smbhash(\@c8, substr($p21, 7, 7));
  $p24 .= smbhash(\@c8, substr($p21, 14, 7));
}

sub permute
{
  my ($out, $in, $p, $n) = @_;
  my $i;

  foreach $i (0..($n-1))
  {
    $out->[$i] = $in->[$p->[$i]-1];
  }
}

sub lshift
{
  my ($d, $count, $n) = @_;
  my (@out, $i);

  foreach $i (0..($n-1))
  {
    $out[$i] = $d->[($i+$count)%$n];
  }
  foreach $i (0..($n-1))
  {
    $d->[$i] = $out[$i];
  }
}

sub xor
{
  my ($out, $in1, $in2, $n) = @_;
  my $i;

  foreach $i (0..($n-1))
  {
    $out->[$i] = $in1->[$i]^$in2->[$i];
  }
}

